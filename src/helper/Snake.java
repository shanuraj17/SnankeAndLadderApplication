package helper;

public class Snake {

    public Integer head;
    public Integer tail;

    public Snake() {
        this.head = 0;
        this.tail = 0;
    }

    public Integer getHead() {
        return head;
    }

    public void setHead(Integer head) {
        this.head = head;
    }

    public Integer getTail() {
        return tail;
    }

    public void setTail(Integer tail) {
        this.tail = tail;
    }
}
